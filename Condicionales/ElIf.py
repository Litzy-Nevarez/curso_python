letra = input("Ingresa una letra:")

if letra.lower() == "a":
    print("Esta volca es la A")
elif letra.lower() == "e":
    print("Esta volca es la E")
elif letra.lower() == "i":
    print("Esta volca es la I")
elif letra.lower() == "o":
    print("Esta volca es la O")
elif letra.lower() == "u":
    print("Esta volca es la U")
else:
    print("No pertenece a las vocales")